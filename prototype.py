#evaluation
def evaluate_food(model, env, episodes=100):
    scores = []

    for _ in range(episodes):
        state = env.reset()
        done = False
        total_food = 0
        steps = 0
        max_steps = 200

        while not done and steps < max_steps:
            steps += 1

            state_tensor = torch.FloatTensor(state).unsqueeze(0)
            with torch.no_grad():
                action = torch.argmax(model(state_tensor)).item()

            state, reward, done, ate_food = env.step(action)

            if ate_food:
                total_food += 1

        # normalized efficiency
        efficiency = steps / (total_food + 1e-6)

        # normalize by grid size
        normalized_eff = efficiency / env.size

        scores.append(1 / normalized_eff)  # higher = better

    return np.mean(scores)

